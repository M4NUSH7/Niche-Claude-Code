#!/usr/bin/env python3
"""sync_harness.py - single-source-of-truth generator for the harness config trees.

Audit fixes #1 (hand-mirrored .claude/.agents trees drift), #6 (model IDs pinned in ~6
places), #12 (duplicate skill registries), #13 (unfilled prompt.md stubs), #14 (python
interpreter pinning).

Source of truth:
  harness/harness.config.json   models, agents, skills, python interpreter
  .claude/                      agent/command/skill/output-style .md content (authored here ONLY)

Generated (never hand-edit):
  .claude/settings.json         model IDs + efforts stamped from config; hook commands use cfg python
  .claude/agents/*.md           frontmatter `model:`/`effort:` stamped from config (body untouched)
  .agents/**                    full tree derived from .claude/ with declared diffs:
                                  - all ".claude/" path strings -> ".agents/"
                                  - adds defaultModel, maxEffort, logging{} block
                                  - BOTH hooks wired (read guard was missing in Coursly's .agents)
  context/skills/index.md       emitted from config.skills
  memory/skills/index.md        emitted from config.skills
  {tree}/logs/{agent}/prompt.md emitted from each agent's .md (frontmatter + system prompt)

Usage:
  py -3 harness/sync_harness.py            # generate/refresh everything
  py -3 harness/sync_harness.py --check    # CI mode: exit 1 if anything on disk drifts
"""
import json
import os
import re
import shutil
import subprocess
import sys

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
CFG_PATH = os.path.join(ROOT, "harness", "harness.config.json")
GEN_MARK = "generated by sync_harness.py - edit harness/harness.config.json or .claude/ sources instead"

MIRRORED_DIRS = ("agents", "commands", "hooks", "output-style")
# Dirs this script fully owns in .agents/. Anything here NOT in the plan is an orphan.
OWNED_AGENT_DIRS = ("agents", "commands", "hooks", "output-style", "logs")

# Re-stamped into every profile, including 'permissive'. These are not preferences.
# History-rewrites and force-pushes destroy work an agent cannot restore; secret reads
# leak into transcripts and logs; .agents/ is generated (hand-edits are silently lost);
# `git tag -d` is gate REVOCATION - a human-approved pivot action (principle #11), never
# an agent's call. deny beats ask beats allow, so these hold regardless of the profile.
NON_NEGOTIABLE_DENY = [
    "Bash(rm -rf /*)",
    "Bash(rm -rf ~*)",
    "Bash(git push --force*)",
    "Bash(git push -f*)",
    "Bash(git reset --hard*)",
    "Bash(git clean -fdx*)",
    "Bash(git tag -d*)",
    "Bash(git push origin --delete*)",
    "Read(**/.env)",
    "Read(**/*.pem)",
    "Read(**/*.key)",
    "Read(**/id_rsa*)",
    "Read(**/.ssh/**)",
    "Edit(.agents/**)",
    "Write(.agents/**)",
]


def load_cfg():
    with open(CFG_PATH, encoding="utf-8") as f:
        return json.load(f)


# ---------- preflight (audit #14, hardened) ----------

def verify_interpreter(cfg):
    """Audit #14, hardened: EXECUTE the interpreter before stamping it into hooks.

    The original template pinned `py -3` to dodge the Windows Store stub. On machines
    without the py launcher that command exits 127 - the hook process never starts, so
    it does not fail *open*, it fails *absent*: no block, no log, no audit trail. The
    guards become decorative and nothing reports it. Never stamp an unverified command.
    """
    py = cfg.get("toolchain", {}).get("python") or cfg.get("python")
    if not py or "REPLACE-ME" in py:
        sys.exit(
            "sync_harness: toolchain.python is unset/placeholder.\n"
            "  Pin an ABSOLUTE path to a project-independent interpreter.\n"
            "  Install one globally:  uv python install 3.11\n"
            "  Never use a bare 'python' (may resolve to a Store stub or a foreign venv)."
        )
    try:
        out = subprocess.run([py, "-c", "import sqlite3,json,glob,re,subprocess,fnmatch,shutil;print('ok')"],
                             capture_output=True, text=True, timeout=30)
    except (OSError, subprocess.SubprocessError) as e:
        sys.exit(f"sync_harness: toolchain.python failed to execute: {py}\n  {e}\n"
                 f"  Install globally: uv python install 3.11")
    if out.returncode != 0 or "ok" not in out.stdout:
        sys.exit(f"sync_harness: toolchain.python is not usable: {py}\n"
                 f"  stdout={out.stdout.strip()!r} stderr={out.stderr.strip()!r}\n"
                 f"  A Microsoft Store stub returns 9009/127 here. Install a real one: uv python install 3.11")
    return py


EFFORT_ENUM = ("low", "medium", "high", "xhigh", "max")


def verify_effort_policy(cfg):
    """A1 hardening: refuse to stamp an invalid effort, refuse maxEffort=='max', and refuse
    any per-agent effort that exceeds maxEffort. Fail loud - a typo'd effort ("mid" is not a
    valid value; the enum is low|medium|high|xhigh|max) previously stamped silently into every
    agent's frontmatter and nobody noticed because nothing validated it.
    """
    max_effort = cfg.get("maxEffort", "high")
    if max_effort not in EFFORT_ENUM:
        sys.exit(f"sync_harness: maxEffort {max_effort!r} is not valid. Enum is {EFFORT_ENUM}.")
    if max_effort == "max":
        sys.exit(
            "sync_harness: maxEffort must never be 'max'. 'max' is a per-task escalation the "
            "user asks for in main chat, never a standing default - a harness shipping 'max' "
            "burns reasoning budget on mechanical work forever, silently. Use 'high'."
        )
    max_rank = EFFORT_ENUM.index(max_effort)
    for name, spec in cfg.get("agents", {}).items():
        effort = spec.get("effort", "high")
        if effort not in EFFORT_ENUM:
            sys.exit(
                f"sync_harness: agents.{name}.effort {effort!r} is not valid.\n"
                f"  Enum is {EFFORT_ENUM}. Fix harness/harness.config.json before syncing."
            )
        if EFFORT_ENUM.index(effort) > max_rank:
            sys.exit(
                f"sync_harness: agents.{name}.effort={effort!r} exceeds maxEffort={max_effort!r}.\n"
                f"  Agents never self-escalate past the ceiling; lower the agent's effort or "
                f"raise maxEffort deliberately (never to 'max')."
            )


def resolve_model(cfg, agent_name):
    spec = cfg["agents"].get(agent_name, {})
    return cfg["models"].get(spec.get("role", "workhorse"), cfg["models"]["workhorse"]), spec.get("effort", "high")


# ---------- .claude/settings.json stamping ----------

def stamped_claude_settings(cfg):
    path = os.path.join(ROOT, ".claude", "settings.json")
    settings = {}
    if os.path.exists(path):
        with open(path, encoding="utf-8") as f:
            settings = json.load(f)

    py = cfg.get("toolchain", {}).get("python") or cfg.get("python", "python")
    # The INTERACTIVE session default is Opus-latest - deliberately NOT models.workhorse.
    # The session default and the per-agent workhorse pin are different things; sharing a
    # value silently downgraded every interactive session to Sonnet.
    settings["model"] = cfg.get("sessionDefault", cfg["models"]["thinker"])

    # Attribution: Claude does not tag itself as a contributor unless asked.
    attr = cfg.get("attribution", {})
    if not attr.get("enabled", False):
        settings["attribution"] = {"commit": attr.get("commit", ""), "pr": attr.get("pr", "")}
    elif "attribution" in settings:
        del settings["attribution"]

    # Permissions: profile 'custom' means the user owns the block - never clobber it.
    if cfg.get("permissions", {}).get("profile") != "custom":
        settings.setdefault("permissions", {})
        settings["permissions"].setdefault("defaultMode", "default")
        for k in ("allow", "ask", "deny"):
            settings["permissions"].setdefault(k, [])
        # deny always wins; keep the guard rails even if a profile widened `allow`
        for rule in NON_NEGOTIABLE_DENY:
            if rule not in settings["permissions"]["deny"]:
                settings["permissions"]["deny"].append(rule)
    # WHOLESALE OVERWRITE, not a merge: this rebuilds the entire hooks block every run, so any
    # hand-added hook in <project>/.claude/settings.json is silently erased on the next sync.
    # Add new hooks HERE (the generator), never to the generated file. Scope is the PROJECT tree
    # only -- ROOT comes from this file's own location, so ~/.claude/settings.json is never read
    # or written, and user-tier hooks (e.g. the token-efficiency rtk-rewrite.sh on PreToolUse/Bash)
    # are out of reach. Those fire ALONGSIDE these -- hooks from all tiers merge and all run.
    # Matchers below deliberately avoid Bash for that reason: two rewriters on the same matcher
    # would fight over each other's output. See 05_hooks_and_logging.md.
    #
    # C1/C3: gate_guard.py's real job is now sensitivePaths + human-gated actions, not general
    # domain policing - each terminal's git worktree (.claude/worktrees/tN/, isolation:
    # "worktree") is the default write sandbox. The matcher below is unchanged (Bash still isn't
    # covered, on purpose - see the comment above), which is exactly why the worktree, not this
    # hook, is the mechanism that actually holds under a Bash write. See "Worktree write-gate
    # (C1/C3)" in 05_hooks_and_logging.md.
    settings["hooks"] = {
        "PreToolUse": [
            {"matcher": "Read",
             "hooks": [{"type": "command", "command": f"{py} .claude/hooks/big_read_guard.py"}]},
            {"matcher": "Write|Edit|MultiEdit|NotebookEdit",
             "hooks": [{"type": "command", "command": f"{py} .claude/hooks/gate_guard.py"}]},
        ],
        "PostToolUse": [
            {"matcher": ".*",
             "hooks": [{"type": "command", "command": f"{py} .claude/hooks/log_tool_use.py"}]},
        ],
        "Stop": [
            {"matcher": "",
             "hooks": [{"type": "command", "command": f"{py} .claude/hooks/fold_logs.py"}]},
        ],
        "SessionStart": [
            {"matcher": "",
             "hooks": [{"type": "command", "command": f"{py} harness/harness_status.py --brief"}]},
        ],
    }
    settings["agents"] = {}
    for name in cfg["agents"]:
        model, effort = resolve_model(cfg, name)
        settings["agents"][name] = {"file": f".claude/agents/{name}.md", "model": model, "effort": effort}
    settings["skills"] = {
        "autoLoad": cfg["skills"].get("autoLoad", []),
        "maxEffort": cfg.get("maxEffort", "high"),
        "effortNote": "Agents never self-escalate past maxEffort; only the user raises it in main chat.",
        "available": cfg["skills"].get("available", []),
    }
    settings["_generated"] = GEN_MARK
    return settings


# ---------- agent frontmatter stamping ----------

def stamp_frontmatter(text, model, effort):
    def repl(field, value, s):
        pat = re.compile(rf"^({field}\s*:).*$", re.MULTILINE)
        if pat.search(s):
            return pat.sub(rf"\1 {value}", s, count=1)
        # insert into frontmatter after the opening ---
        return re.sub(r"^---\s*\n", f"---\n{field}: {value}\n", s, count=1)
    m = re.match(r"^---\n.*?\n---", text, re.DOTALL)
    if not m:
        return text
    fm = m.group(0)
    fm = repl("model", model, fm)
    fm = repl("effort", effort, fm)
    return fm + text[m.end():]


# ---------- .agents/ derivation ----------

def derive_agents_settings(cfg, claude_settings):
    s = json.loads(json.dumps(claude_settings).replace(".claude/", ".agents/"))
    s["defaultModel"] = cfg["models"]["workhorse"]
    s["maxEffort"] = cfg.get("maxEffort", "high")
    s["logging"] = {"backend": "sqlite", "basePath": ".agents/logs", "perAgent": True}
    s["_generated"] = GEN_MARK
    return s


# ---------- emitted indexes / stubs ----------

def skills_index_md(cfg, flavor):
    # `path` is an OPTIONAL override (see config._skillsPolicy): omit it and the skill resolves by
    # name from whichever store has it (project .skills/ -> user ~/.claude/skills/ -> built-in).
    # .get() not [] -- a required path was the old shape and KeyErrors on a name-resolved entry.
    rows = "\n".join(
        f"| {s['name']} | {s.get('loadOn', '-')} | {'`' + s['path'] + '`' if s.get('path') else 'by name'} |"
        for s in cfg["skills"].get("available", [])
    )
    auto = ", ".join(cfg["skills"].get("autoLoad", [])) or "-"
    return (f"<!-- {GEN_MARK} -->\n\n# Skills Index ({flavor})\n\n"
            f"Auto-loaded: **{auto}**. Everything else loads ONLY on its trigger; announce "
            f"\"Loading skill: <name>\" then read its file. Never read a skill's reference/ "
            f"sub-files until the skill routes you there.\n\n"
            f"| Skill | Load condition | Path |\n|---|---|---|\n{rows}\n")


def prompt_stub(agent_name, agent_md, model, effort):
    body = re.sub(r"^---\n.*?\n---\s*", "", agent_md, count=1, flags=re.DOTALL)
    return (f"---\nagent: {agent_name}\nmodel: {model}\neffort: {effort}\n---\n\n"
            f"<!-- {GEN_MARK} -->\n\n# System prompt (copy of .claude/agents/{agent_name}.md)\n\n"
            + body.strip() + "\n")


# ---------- plan / apply / check ----------

def build_plan(cfg):
    """Return {relpath: content(str) | ('copy', srcpath)} for everything generated."""
    plan = {}
    claude_settings = stamped_claude_settings(cfg)
    plan[".claude/settings.json"] = json.dumps(claude_settings, indent=2) + "\n"
    plan[".agents/settings.json"] = json.dumps(derive_agents_settings(cfg, claude_settings), indent=2) + "\n"

    # stamped agent files + prompt stubs, both trees
    agents_dir = os.path.join(ROOT, ".claude", "agents")
    if os.path.isdir(agents_dir):
        for fn in sorted(os.listdir(agents_dir)):
            if not fn.endswith(".md"):
                continue
            name = fn[:-3]
            model, effort = resolve_model(cfg, name)
            with open(os.path.join(agents_dir, fn), encoding="utf-8") as f:
                stamped = stamp_frontmatter(f.read(), model, effort)
            plan[f".claude/agents/{fn}"] = stamped
            plan[f".agents/agents/{fn}"] = stamped
            stub = prompt_stub(name, stamped, model, effort)
            plan[f".claude/logs/{name}/prompt.md"] = stub
            plan[f".agents/logs/{name}/prompt.md"] = stub

    # mirrored dirs (verbatim copies .claude -> .agents)
    for d in ("commands", "hooks", "output-style"):
        src_dir = os.path.join(ROOT, ".claude", d)
        if os.path.isdir(src_dir):
            for fn in sorted(os.listdir(src_dir)):
                src = os.path.join(src_dir, fn)
                if os.path.isfile(src):
                    plan[f".agents/{d}/{fn}"] = ("copy", src)

    plan["context/skills/index.md"] = skills_index_md(cfg, "context")
    plan["memory/skills/index.md"] = skills_index_md(cfg, "memory")
    return plan


def content_of(item):
    if isinstance(item, tuple) and item[0] == "copy":
        with open(item[1], encoding="utf-8", errors="ignore") as f:
            return f.read()
    return item


def apply_plan(plan):
    for rel, item in plan.items():
        dst = os.path.join(ROOT, rel)
        os.makedirs(os.path.dirname(dst), exist_ok=True)
        if isinstance(item, tuple) and item[0] == "copy":
            shutil.copyfile(item[1], dst)
        else:
            with open(dst, "w", encoding="utf-8", newline="\n") as f:
                f.write(item)
    print(f"sync_harness: wrote {len(plan)} files.")


def find_orphans(plan):
    """Files on disk in dirs we OWN that the plan does not produce.

    build_plan() enumerates from .claude/, so a deleted source silently drops out of the
    plan and its stale .agents/ twin passes --check forever. That is a hole in audit #1's
    'CI fails on drift' invariant, exactly where hand-editing is most tempting.
    """
    planned = {os.path.normcase(os.path.join(ROOT, p)) for p in plan}
    orphans = []
    for d in OWNED_AGENT_DIRS:
        base = os.path.join(ROOT, ".agents", d)
        for dirpath, _, files in os.walk(base):
            for fn in files:
                full = os.path.join(dirpath, fn)
                if fn.endswith(".pyc") or "__pycache__" in dirpath:
                    continue
                if os.path.normcase(full) not in planned:
                    orphans.append(os.path.relpath(full, ROOT).replace("\\", "/"))
    return sorted(orphans)


def check_plan(plan, py="python"):
    drift = []
    for rel, item in plan.items():
        dst = os.path.join(ROOT, rel)
        if not os.path.exists(dst):
            drift.append(f"MISSING  {rel}")
            continue
        with open(dst, encoding="utf-8", errors="ignore") as f:
            if f.read() != content_of(item):
                drift.append(f"DRIFTED  {rel}")
    for o in find_orphans(plan):
        drift.append(f"ORPHAN   {o}  (not produced by the plan - its .claude/ source was deleted)")
    if drift:
        print(f"sync_harness --check FAILED - regenerate with: {py} harness/sync_harness.py")
        print("\n".join(drift))
        return 1
    print(f"sync_harness --check OK ({len(plan)} files match, no orphans).")
    return 0


def verify_project_name(cfg):
    """N4: only the python placeholder was ever checked. 'project' is stamped into generated
    files too (README headers, CI, prompt stubs) - an unedited REPLACE-ME silently ships."""
    name = cfg.get("project")
    if not name or name == "REPLACE-ME":
        sys.exit(
            "sync_harness: harness.config.json 'project' is unset/placeholder ('REPLACE-ME').\n"
            "  Set it to the real project name before syncing."
        )


def verify_terminal_topology(cfg):
    """Terminal-cap framing (owner's headline ask): at least one terminal must be neverBlocked,
    or a stalled gate stalls the entire build. Fail loud rather than silently shipping a harness
    where every terminal can deadlock on a gate nobody opened yet."""
    terminals = cfg.get("terminals", {})
    if not terminals:
        return  # nothing to check yet (pre-config bootstrap) - verify_init.py covers this too
    if not any(spec.get("neverBlocked") for spec in terminals.values()):
        sys.exit(
            "sync_harness: no terminal has neverBlocked:true.\n"
            "  Terminals scale to the work - 1 if the domains don't split, N if they do - but "
            "the hard rule is at least one terminal must never block, or a stalled gate stalls "
            "the whole build. Mark the terminal that builds against contracts/schemas "
            "(commonly the product/frontend plane) neverBlocked: true."
        )


def main():
    cfg = load_cfg()
    py = verify_interpreter(cfg)  # fail loud before generating anything
    verify_project_name(cfg)
    verify_effort_policy(cfg)
    verify_terminal_topology(cfg)
    plan = build_plan(cfg)
    if "--check" in sys.argv:
        return check_plan(plan, py)
    apply_plan(plan)
    return 0


if __name__ == "__main__":
    sys.exit(main())
